CREATE TABLE PC(
	model CHAR(10) PRIMARY KEY,
	speed INT,
	ram INT,
	hd INT,
	rd CHAR(10),
	price INT,
        FOREIGN KEY (model) REFERENCES Product(model)
);


CREATE TABLE Laptop(
	model CHAR(10) PRIMARY KEY,
	speed INT,
	ram INT,
	hd INT,
	screen number(4,2),
	price INT,
        FOREIGN KEY (model) REFERENCES Product(model)
);

ALTER TABLE LAPTOP
    ADD CONSTRAINT check_laptop_price check ( PRICE >= 500 );

